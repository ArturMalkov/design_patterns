"""
KISS - keep it simple, stupid (or keep it short and simple).
1) There's no point in:
    - implementing additional features;
    - overloading interface with options which won't be used by the majority of users;
    - implementing complex business logic which considers all possible options;
    - ceaselessly increasing the level of abstraction (infinite inheritance hierarchy);
    - implementing excessive features (in advance);
    - using a large library if you just need a couple of its functions;
    - mathematical precision or ultimate specification - precision design is fragile.
2) Decompose complex things into simpler ones.
3) Simple design:
    - no duplicates in logic;
    - all important intentions are clearly visible;
    - least possible number of classes and methods.
"""